public class Challenge1 {
    public static void main(String[] args) {
        char letter = 'a';

        switch (letter){
            case 'a':
                System.out.println("Able");
                break;
            case 'b':
                System.out.println("Baker");
                break;
            case 'c':
                System.out.println("Charlie");
                break;
            case 'd':
                System.out.println("Dog");
                break;
        }
    }

}
